function argbToHex(argb: string): string {
  // ÊèêÂèñ ARGB ‰∏≠ÁöÑ R„ÄÅG„ÄÅB ÈÉ®ÂàÜ
  const red = argb.slice(2, 4);    // ÂèñÁ∫¢Ëâ≤ÈÉ®ÂàÜ
  const green = argb.slice(4, 6);  // ÂèñÁªøËâ≤ÈÉ®ÂàÜ
  const blue = argb.slice(6, 8);   // ÂèñËìùËâ≤ÈÉ®ÂàÜ

  // ÊãºÊé•Êàê #RRGGBB Ê†ºÂºèÂπ∂ËøîÂõû
  return `#${red}${green}${blue}`;
}
